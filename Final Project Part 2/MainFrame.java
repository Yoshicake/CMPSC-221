    public void rebuildStudentComboBoxes(){
        SelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        selectStudentScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        StudentDropComboBox.setModel(new javax.swing.DefaultComboBoxModel());
        DropClassStudentSelect.setModel(new javax.swing.DefaultComboBoxModel());
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        for (StudentEntry student : students)
        {    
            SelectStudentComboBox.addItem(student.getLastName() + ", " + student.getFirstName());
            selectStudentScheduleComboBox.addItem(student.getLastName() + ", " + student.getFirstName());
            StudentDropComboBox.addItem(student.getLastName() + ", " + student.getFirstName());
            DropClassStudentSelect.addItem(student.getLastName() + ", " + student.getFirstName() + "  " + student.getStudentID());
        }
    }

