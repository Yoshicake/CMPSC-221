    private void DropStudentAndClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentAndClassButtonActionPerformed
        StudentEntry student = getStudentFromComboBox(DropClassStudentSelect.getSelectedIndex());
        String classCode = String.valueOf(DropClassStudentComboBox.getSelectedItem());
        boolean isFound = false;
        boolean isScheduled = false;
        String existingText = "Error: student is not scheduled in this class.";
        ArrayList<StudentEntry> waitlistedStudents = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester, classCode);
        ArrayList<StudentEntry> scheduled = MultiTableQueries.getScheduledStudentsByClass(currentSemester, classCode);
        ArrayList<ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, classCode);
        for (StudentEntry s : scheduled) {
            if ((s.getStudentID()).equals(student.getStudentID())){
                isFound = true;
                isScheduled = true;
            }
        }
        
        if (!isFound) {
            for (StudentEntry s : waitlistedStudents) {
                if ((s.getStudentID()).equals(student.getStudentID())){
                    isFound = true;
                    isScheduled = false;
                }
            }
        }
        
        if (isFound){
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester,student.getStudentID(),classCode);
            existingText = student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + " has been dropped from " + classCode + ".";
        }
        
        String additionalText = " ";
        if (!waitlist.isEmpty() && isScheduled) {
                    ScheduleQueries.updateScheduleEntry(waitlist.get(0));
                    StudentEntry stu2 = StudentQueries.getStudent((waitlist.get(0)).getStudentID());
                    additionalText = "\n" + stu2.getLastName() + ", " + stu2.getFirstName() + " " + stu2.getStudentID() + " has been scheduled into " + classCode + ".";
                    existingText = existingText + additionalText;
        }
        DropClassStudentTextArea.setText(existingText);
    }//GEN-LAST:event_DropStudentAndClassButtonActionPerformed

    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            project = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            project = "Course Scheduler Spring 2024";

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(project.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

